const mongoose = require('mongoose');
mongoose.Promise = global.Promise;

const _ = require('underscore');

let DomoModel = {};

// Function pointer that converts string id into model id
const convertId = mongoose.Types.ObjectId;
const setName = (name) => _.escape(name).trim();

const DomoSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
    set: setName,
  },
  age: {
    type: Number,
    min: 0,
    required: true,
  },
  owner: {
    type: mongoose.Schema.ObjectId,
    required: true,
    ref: 'Account',
  },
  favoriteFood: {
    type: String,
    default: 'unknown',
  },
  leastFavoriteFood: {
    type: String,
    default: 'unknown',
  },
  createdDate: {
    type: Date,
    default: Date.now,
  },
});

DomoSchema.statics.toAPI = (doc) => ({
  name: doc.name,
  age: doc.age,
  favoriteFood: doc.favoriteFood,
  leastFavoriteFood: doc.leastFavoriteFood,
});

DomoSchema.statics.findByOwner = (ownerId, callback) => {
  const search = {
    owner: convertId(ownerId),
  };

  return DomoModel.find(search).select('name age favoriteFood leastFavoriteFood').exec(callback);
};

DomoModel = mongoose.model('Domo', DomoSchema);

module.exports.DomoModel = DomoModel;
module.exports.DomoSchema = DomoSchema;
